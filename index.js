const generateTextBtn = document.querySelector(".btn");
const allCheckboxes = document.querySelectorAll("input[type=checkbox]");
const textContent = document.querySelector(".content");
const textContainer = document.querySelector(".text-container");
let checked = [];

generateTextBtn.addEventListener('click', function() {
    textContent.innerHTML = "";
    allCheckboxes.forEach(el => { if (el.checked) { checked.push(el) } });

    for (let i = 0; i < checked.length; i++) {
        texts.forEach(el => {
            let html = `<div class="single">
            <div class="title">${el.title}</div>
            <p>${el.description}</p>
        </div>`;
            if (el.value === checked[i].value) {
                textContent.insertAdjacentHTML("beforeend", html);
            }
        });
    }
    textContainer.style.opacity = "1";
    checked = [];
});